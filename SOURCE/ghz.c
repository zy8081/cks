void puthz_coverd(int x, int y,char *s,int n,int flag,int part,int color)
{
	int i=0;
	for(i=0;i<part*n;i++)
	{
		Line_Thick(x+i,y,x+i,y+flag+1,1,Getpixel64k(x+i,y-1));
	}
	puthz( x,  y, s, flag, part, color);

}
